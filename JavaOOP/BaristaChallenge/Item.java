import java.util.ArrayList;

public class Item {
	private String name;
	private double price;


	//Constructor
	public Item(String name, double price) {
		this.name = name;
		this.price = price;
	}

	//Getters and Setters
	public String getName() {
		return name;
	}

	public double getPrice() {
		return price;
	}

	public void setName() {
		this.name = name;
	}

	public void setPrice() {
		this.price = price;
	}
}
